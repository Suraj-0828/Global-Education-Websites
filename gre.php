                                        <h3 class="title">What is GRE?</h3>
                                        <p>The GRE (Graduate Record Examination) is a standardized test that is an admission requirement for many graduate schools, primarily in the United States but also in several other countries. It assesses a student's readiness for graduate-level academic work and is commonly used by universities for admission to master's, MBA, and Ph.D. programs.</p>

                                        <h4>Structure of the GRE</h4>
                                        <p>The GRE is divided into three main sections:</p>
