                                    <h3 class="title">What is SAT?</h3>
<p>The SAT (Scholastic Assessment Test) is a standardized test widely used for college admissions in the United States and other countries. It assesses students' readiness for college and provides colleges with a common data point to compare applicants.</p>

<h4>SAT Structure:</h4>
<ul>
    <li><strong>Evidence-Based Reading and Writing (EBRW):</strong>
        <ul>
            <li><strong>Reading Test:</strong>
                <ul>
                    <li>52 multiple-choice questions.</li>
                    <li>Includes passages from literature, history, social studies, and sciences.</li>
                    <li>Tests reading comprehension, understanding of complex texts, and analytical skills.</li>
                </ul>
            </li>
            <li><strong>Writing and Language Test:</strong>
                <ul>
                    <li>44 multiple-choice questions.</li>
                    <li>Focuses on grammar, vocabulary, sentence structure, and editing skills.</li>
                </ul>
            </li>
        </ul>
    </li>
    <li><strong>Math:</strong>
        <ul>
            <li><strong>No-Calculator Section:</strong>
                <ul>
                    <li>20 questions.</li>
                    <li>Tests algebra, arithmetic, and data analysis skills without a calculator.</li>
                </ul>
            </li>
            <li><strong>Calculator Section:</strong>
                <ul>
                    <li>38 questions.</li>
                    <li>Includes more advanced math like trigonometry and complex algebra, with a calculator allowed.</li>
                </ul>
            </li>
        </ul>
    </li>
    <li><strong>Optional Essay:</strong>
        <ul>
            <li>The SAT used to include an optional essay, but it has been discontinued in most places starting from 2021.</li>
        </ul>
    </li>
</ul>

<h4>SAT Scoring:</h4>
<ul>
    <li>Each section (EBRW and Math) is scored on a scale of 200 to 800.</li>
    <li>The combined score ranges from 400 to 1600.</li>
</ul>

<h4>How to Take the SAT:</h4>
<ol>
    <li><strong>Register for the SAT:</strong>
        <ul>
            <li>Go to the official College Board website collegeboard org.</li>
            <li>Create an account, choose your test date, and select a test center or the online option if available.</li>
        </ul>
    </li>
    <li><strong>Choose a Test Date:</strong>
        <ul>
            <li>The SAT is offered multiple times a year (usually seven times) around the world. Choose a date that gives you enough time to prepare.</li>
        </ul>
    </li>
    <li><strong>Preparation:</strong>
        <ul>
            <li>Use SAT prep books, online courses, and practice tests.</li>
            <li>Khan Academy offers free, personalized SAT preparation.</li>
        </ul>
    </li>
    <li><strong>Test Day:</strong>
        <ul>
            <li>Bring your admission ticket, ID, and allowed equipment (such as calculators).</li>
            <li>Arrive at the test center on time.</li>
        </ul>
    </li>
    <li><strong>Score Reporting:</strong>
        <ul>
            <li>Scores are available online about two to three weeks after the test.</li>
            <li>You can send your scores to colleges directly from the College Board portal.</li>
        </ul>
    </li>
</ol>

<h4>Tips for SAT Preparation:</h4>
<ul>
    <li>Practice with full-length tests to get familiar with the timing and question types.</li>
    <li>Focus on areas where you need improvement, and review the most commonly tested concepts.</li>
</ul>



