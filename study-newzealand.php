                                        <h3 class="title">Scholarships in New Zealand</h3>
                                        <p>New Zealand provides a variety of scholarships to international students to help them finance their education. These scholarships are offered by the New Zealand government, universities, and other organizations. Here are some of the notable scholarships available for international students:</p>
                                        <h3 class="title">New Zealand Scholarships for International Tertiary Students</h3>
                                        <p>The New Zealand Government provides scholarships for international students from selected developing countries to study at New Zealand universities or technical institutions. These scholarships cover tuition fees, travel costs, living expenses, and insurance. The goal is to provide students with the knowledge and skills they need to contribute to the development of their home country.</p>
                                        <h3 class="title">New Zealand Excellence Awards (NZEA)</h3>
                                        <p>The New Zealand Excellence Awards are available for students from India and Southeast Asia and are funded by Education New Zealand (ENZ) and New Zealand universities. The scholarship aims to attract high-achieving students and offers partial financial support for undergraduate and postgraduate courses across a range of disciplines.</p>
                                        <h3 class="title">University of Auckland International Student Excellence Scholarship</h3>
                                        <p>The University of Auckland offers this scholarship to new international students who are applying for undergraduate or postgraduate taught study. The scholarship is valued at up to NZD 10,000 and is awarded based on academic merit. It aims to attract talented students from around the world to study at the University of Auckland.</p>
                                        <h3 class="title">Victoria University of Wellington Scholarships</h3>
                                        <p>Victoria University of Wellington offers various scholarships for international students, such as the Wellington International Excellence Scholarship for high-achieving students and the Victoria Master’s by Thesis Scholarship for students undertaking a thesis-based master’s degree. These scholarships provide financial assistance to cover part of the tuition fees.</p>
                                        <h3 class="title">University of Otago International Research Master’s and PhD Scholarships</h3>
                                        <p>The University of Otago offers scholarships to international students who are undertaking research master’s degrees or Ph.D. programs. The scholarships cover tuition fees and provide a living allowance. They are awarded based on academic excellence and research potential.</p>
                                        <h3 class="title">University of Waikato International Excellence Scholarship</h3>
                                        <p>The University of Waikato offers the International Excellence Scholarship to high-achieving international students who are enrolling in undergraduate or postgraduate taught programs. The scholarship provides up to NZD 10,000 towards tuition fees and is awarded based on academic merit.</p>
                                        <h3 class="title">Lincoln University International Scholarships</h3>
                                        <p>Lincoln University provides a range of scholarships for international students, such as the International Undergraduate Scholarship and International Taught Master Merit Scholarship. These scholarships are designed to support high-achieving students and cover a portion of the tuition fees.</p>
                                        <h3 class="title">AUT International Excellence Scholarships</h3>
                                        <p>Auckland University of Technology (AUT) offers several scholarships to support international students, including the International Excellence Scholarship. This scholarship is awarded to high-achieving international students and covers part of the tuition costs for undergraduate and postgraduate programs.</p>
                                        
