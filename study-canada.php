                                        <h3 class="title">Work Opportunities During and After Studies:</h3>
                                        <p>One of the biggest advantages of studying in Canada is the opportunity to work while studying. International students can work up to 20 hours per week during academic sessions and full-time during scheduled breaks, such as summer or winter holidays, without the need for an additional work permit. This helps students support themselves financially and gain valuable work experience. Upon graduation, students can apply for the Post-Graduation Work Permit (PGWP), which allows them to work in Canada for up to three years. The PGWP is an excellent way for graduates to gain Canadian work experience, which can also lead to permanent residency.
                                        </p>

