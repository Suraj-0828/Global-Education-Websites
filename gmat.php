                                                </ul>
                                            </li>
                                            <li><strong>Quantitative Reasoning:</strong>
                                                <ul>
                                                    <li>31 questions.</li>
                                                    <li>Focuses on arithmetic, algebra, and geometry.</li>
                                                    <li>Problem-solving and data sufficiency questions.</li>
                                                </ul>
                                            </li>
                                            <li><strong>Verbal Reasoning:</strong>
                                                <ul>
                                                    <li>36 questions.</li>
                                                    <li>Tests reading comprehension, critical reasoning, and sentence correction.</li>
                                                </ul>
                                            </li>
                                        </ul>

